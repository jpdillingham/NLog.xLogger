using System.Diagnostics;
using NLog;

namespace NLog.xLogger
{
    /// <summary>
    ///     Wraps NLog.LogManager for convenience when instantiating xLogger instances.
    /// </summary>
    public static class xLogManager
    {
        /// <summary>
        ///     Mimics <see cref="NLog.LogManager.GetCurrentClassLogger"/>; returns an instance of <see cref="xLogger"/>.
        /// </summary>
        /// <returns>The requested instance of xLogger.</returns>
        public static xLogger GetCurrentClassxLogger()
        {
            return (xLogger)LogManager.GetLogger(new StackTrace().GetFrame(1).GetMethod().ReflectedType.FullName, typeof(xLogger));
        }

        /// <summary>
        ///     Mimics <see cref="NLog.LogManager.GetLogger"/>; returns an instance of <see cref="xLogger"/> with the specified name.
        /// </summary>
        /// <param name="name">Name of the logger.</param>
        /// <returns>The requested instance of xLogger.</returns>
        public static xLogger GetxLogger(string name)
        {
            return (xLogger)LogManager.GetLogger(name, typeof(xLogger));
        }
    }
}
